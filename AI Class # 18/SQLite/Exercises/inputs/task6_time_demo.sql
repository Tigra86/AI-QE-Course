-- 07. Age using SQLite
SELECT (STRFTIME('%s', 'now') - STRFTIME('%s', '2015-10-10')) / 31557600; -- 10

SELECT STRFTIME('%s', 'now');            -- 1773973071
SELECT STRFTIME('%s', '1970-01-01');     -- 0
SELECT STRFTIME('%s', '2015-10-10');     -- 1444435200
SELECT 1773973071 - 1444435200;          -- 329105871
SELECT 365.25 * 24 * 60 * 60;            -- 31557600
SELECT 329105871 / 31557600;             -- 10

SELECT FLOOR((JULIANDAY('now') - JULIANDAY('2015-10-10')) / 365.25);

SELECT JULIANDAY('now'); -- 2461119.5980950114
SELECT JULIANDAY('2015-10-10');          -- 2457305.5
SELECT 2461119.5980950114 - 2457305.5;   -- 3814.0980950114317
SELECT 3814.0980950114317 / 365.25;      -- 10.442431471626097
SELECT FLOOR(10.442431471626097);        -- 10